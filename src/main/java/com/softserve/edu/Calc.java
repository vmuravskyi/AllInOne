package com.softserve.edu;

/**
 * <>Calculator.</>
 */
public final class Calc {
    /**
     * @param arg0 is first parameter for calculation.
     * @param arg1 is first parameter for calculation.
     * @return the result of calculation.
     */
    public double add(final double arg0, final double arg1) {
        return arg0 + arg0;
    }

    public double div(final int arg0, final int arg1) {
        return arg0 / arg1;
    }
}
